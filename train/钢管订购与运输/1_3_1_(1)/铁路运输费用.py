def price(distance):
    if distance == 0:
        return 0
    if distance <= 300:
        return 20
    elif distance <= 350:
        return 23
    elif distance <= 400:
        return 26
    elif distance <= 450:
        return 29
    elif distance <= 500:
        return 32
    elif distance <= 600:
        return 37
    elif distance <= 700:
        return 44
    elif distance <= 800:
        return 50
    elif distance <= 900:
        return 55
    elif distance <= 1000:
        return 60
    else:
        return 60 + 5 * ((distance - 1000) // 100)


